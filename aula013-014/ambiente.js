console.log('Com o while')

var c = 1
while (c <= 5) {
    console.log(`Passo ${c} `)
    c++
}

console.log('Com o do while')

c = 1

do {
    console.log(`Passo ${c} `)
    c++
} while (c <= 5)


//AULA 014

console.log('Com o for')

for (var c = 1; c <= 5; c++) {
    console.log(`Passo ${c}`)
}